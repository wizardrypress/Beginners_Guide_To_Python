# Example 04: Python operators

a = 10
b = 3

print("Arithmetic operators:")
print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")
print(f"{a} % {b} = {a % b}")
print(f"{a} ** {b} = {a ** b}")

print()
print("Comparison operators:")
print(f"{a} == {b}: {a == b}")
print(f"{a} != {b}: {a != b}")
print(f"{a} > {b}: {a > b}")
print(f"{a} < {b}: {a < b}")

print()
print("Logical operators:")
is_adult = True
has_ticket = False

print(f"is_adult and has_ticket: {is_adult and has_ticket}")
print(f"is_adult or has_ticket: {is_adult or has_ticket}")
print(f"not has_ticket: {not has_ticket}")
