# Ejercicio 5: Operaciones aritméticas
# Realiza operaciones aritméticas básicas.

numero_1 = float(input("Ingresa el primer número: "))
numero_2 = float(input("Ingresa el segundo número: "))

print("Suma:", numero_1 + numero_2)
print("Resta:", numero_1 - numero_2)
print("Multiplicación:", numero_1 * numero_2)
print("División:", numero_1 / numero_2)
print("Exponente:", numero_1 ** numero_2)
